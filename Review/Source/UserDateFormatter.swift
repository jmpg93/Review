import Foundation

class UserDateFormatter : DateFormatter {
    override init() {
        super.init()
        commonInit()
    }
    
    required init?(coder aDecoder: NSCoder) {
        super.init(coder: aDecoder)
        commonInit()
    }
    
    func commonInit() {
        dateFormat = "yyyy-MM-dd'T'HH:mm:ss"
    }
}
